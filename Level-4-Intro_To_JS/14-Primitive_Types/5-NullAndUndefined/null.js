// Null is a special value that indicates the absence of value
// null is empty or non-extent value. It MUST be asigned
let a = null;
console.log(a);
console.log(typeof a);
a = 5;
console.log(a);
console.log(typeof a);

// undefined - indicates that a variable has not been initialized and the value is absent
// variable has been declared, but not defined
let b;
